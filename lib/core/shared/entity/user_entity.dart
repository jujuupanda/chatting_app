class UserEntity {
  final String id;
  final String email;
  final String image;
  final String phone;
  final String firstName;
  final String middleName;
  final String lastName;
  final String information;

  UserEntity({
    required this.id,
    required this.email,
    required this.image,
    required this.phone,
    required this.firstName,
    required this.middleName,
    required this.lastName,
    required this.information,
  });
}
